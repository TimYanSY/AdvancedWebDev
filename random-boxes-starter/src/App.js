import React, { Component } from 'react';
import './App.css';
import Box from './box'

const randIndex = (array) => Math.floor(array.length * Math.random())

function shuffle(a) {
    var j, x, i;
    for (i = a.length - 1; i > 0; i--) {
        j = Math.floor(Math.random() * (i + 1));
        x = a[i];
        a[i] = a[j];
        a[j] = x;
    }
}

class App extends Component {

  constructor(props) {
    super(props);
    this.state = {
      allColors: this.props.allColors
    }

    setInterval(() => {
      const newColors = this.state.allColors.slice();
      shuffle(newColors);
      this.setState ({
        allColors: newColors
      })
    }, 2000);
  };

  render() {
    const boxes = [];
    for (let i = 0; i < 32; i++) {
      boxes.push(
        <Box
          key={i}
          color={this.state.allColors[randIndex(this.state.allColors)]}
        />
      )
    }

    return (
      <div className="App">
      {boxes}
      </div>
    );
  }
}

